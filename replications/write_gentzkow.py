a('}')
a('else {')
a(f'    di "twowayfeweights (T=year) FAILED rc=" {bt("tw_rc1")}')
a('    local tw_beta1  = .')
a('    local tw_npos1  = .')
a('    local tw_nneg1  = .')
a('    local tw_pneg1  = "N/A"')
a('}')
a('')
a('* T=styr version (matches paper spec)')
a('cap noisily twowayfeweights prestout cnty90 styr numdailies, type(feTR)')
