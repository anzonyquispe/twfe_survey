a('set more off')
a('set matsize 5000')
a('')
a('adopath + "C:/Users/Usuario/Documents/GitHub/twfe_survey/data/2010-2012/Gentzkow et al. (2011)/20091316_data/external"')
a('loadglob using "C:/Users/Usuario/Documents/GitHub/twfe_survey/data/2010-2012/Gentzkow et al. (2011)/20091316_data/code/input_param.txt"')
